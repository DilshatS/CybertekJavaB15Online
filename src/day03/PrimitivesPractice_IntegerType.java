package day03;

public class PrimitivesPractice_IntegerType {

    public static void main(String[] args) {

        byte letterCount = 26;
        System.out.println("The letter count is " + letterCount);

        short sheepCount = 300;
        System.out.println("sheepCount = " + sheepCount);

        int catCount = 20;
        System.out.println("catCount = " + catCount);

        long mileToMoon = 5000000L;
        System.out.println("mileToMoon = " + mileToMoon);

        float priceOfBanana = 1.99f;
        System.out.println("priceOfBanana = " + priceOfBanana);

        double priceOfIpad1 = 355.99d;
        System.out.println("priceOfIpad1 = " + priceOfIpad1);

    }

}
