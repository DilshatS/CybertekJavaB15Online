package day07;

public class RelationalOperator {

    public static void main(String[] args) {

        // >, >=, <, <=, ==, != Relational Operator

        // Relation Operator is used to compare 2 operands
        // and return true or false
        // anything action or expression that return true or false result
        // can be called boolean expression

        System.out.println( 7 > 5 );
        System.out.println( 7 <- 19 );
        System.out.println( 5 >= 5 );
        System.out.println( 7 >= 8 );
        System.out.println( 4 <= 5 );
        System.out.println( 17 >= 5 );

        //  == is used to check equality
        // AND VERY DIFFERENT FROM SINGLE

        // difference between == and = same as difference between equal and equality daily English
        System.out.println( 5 == 5 );

        // != is used to check for inequality, ! in programming translate to NOT
        System.out.println( 6 != 10 );
        System.out.println( 6 != 6 );


    }

}
